import turtle

bob = turtle.Turtle()  # Capital "T" in Turtle

bob.forward(100)  # Move forward by 100 units
bob.left(90)      # Turn left by 90 degrees
bob.forward(100)  # Move forward by another 100 units
bob.left(90)      # Turn left by 90 degrees again
bob.forward(100)  # Move forward by 100 units again
bob.left(90)      # Turn left by 90 degrees again